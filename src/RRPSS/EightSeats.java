package RRPSS;

/**
 * Represents a Table Class with EIGHT seats.
 * @author YANGY
 *
 */

public class EightSeats extends Table {
	/**
	 * Constructor which creates a Table with EIGHT seats.
	 * @param tableNo this Table's number
	 */
	public EightSeats(int tableNo) {
		super(tableNo);
		seats = 8;
	}
}
