package RRPSS;

/**
 * Represents a Table Class with FOUR seats.
 * @author YANGY
 *
 */
public class FourSeats extends Table {
	/**
	 * Constructor which creates a Table with EIGHT seats.
	 * @param tableNo this Table's number
	 */
	public FourSeats(int tableNo) {
		super(tableNo);
		seats = 4;
	}
}
